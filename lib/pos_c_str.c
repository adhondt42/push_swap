#include "../inc/push_swap.h"

int				pos_c_str(char c, char *str)
{
	int			i;

	i = 0;
	if (c == 0 || !str)
		return (-1);
	while (str[i])
	{
		if (str[i] == c)
			return (i);
		else
			i++;
	}
	return (-1);
}
