#include "../inc/push_swap.h"

int		intlen(int nb)
{
	int	size;

	size = 1;
	if (nb < 0)
	{
		size++;
		nb *= -1;
	}
	while (nb / 10)
	{
		size++;
		nb /= 10;
	}
	return (size);
}
