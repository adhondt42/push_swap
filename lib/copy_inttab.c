#include "../inc/push_swap.h"

int			*copy_inttab(int *a, int alen)
{
	int		i;
	int		*dest;

	i = -1;
	dest = ft_intnew(alen, 0);
	while (++i < alen)
		dest[i] = a[i];
	return (dest);
}
