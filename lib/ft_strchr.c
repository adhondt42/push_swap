#include "../inc/push_swap.h"

char	*ft_strchr(const char *s, int c)
{
	char	*cs;

	if (!s)
		return (NULL);
	cs = (char *)s;
	while (*cs != c && *cs)
		cs++;
	if (*cs == c)
		return (cs);
	return (NULL);
}
